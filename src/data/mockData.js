export const NOTIFICATIONS = (() => {
  const notifs = [];
  let id = 1;

  HIVES_DATA.forEach(hive => {
    if (hive.status === 'critical') {
      notifs.push({
        id: id++,
        type: 'critical',
        hiveId: hive.id,
        message: hive.alertType || 'Kritik durum algÄ±landÄ±',
        time: hive.lastUpdate,
        read: false
      });
    } else if (hive.status === 'warning') {
      notifs.push({
        id: id++,
        type: 'warning',
        hiveId: hive.id,
        message: hive.alertType || 'UyarÄ± durumu',
        time: hive.lastUpdate,
        read: false
      });
    }
  });

  // DÃ¼ÅŸÃ¼k pil bildirimi
  HIVES_DATA.filter(h => h.battery < 20).forEach(hive => {
    if (!notifs.find(n => n.hiveId === hive.id && n.message.includes('Pil'))) {
      notifs.push({
        id: id++,
        type: 'warning',
        hiveId: hive.id,
        message: `DÃ¼ÅŸÃ¼k Pil Seviyesi (%${hive.battery})`,
        time: hive.lastUpdate,
        read: true
      });
    }
  });

  return notifs;
})();

