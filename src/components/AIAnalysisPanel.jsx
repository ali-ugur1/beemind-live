// ─── Component ───────────────────────────────────────────────────────────
const AIAnalysisPanel = ({ isOpen, onClose, hive }) => {
  const { lang } = useLanguage();
  const t = lang === "tr" ? TR : EN;
  const closeBtnRef = useRef(null);

  // ESC to close + scroll lock + initial focus
  useEffect(() => {
    if (!isOpen) return;

    const handleKey = (e) => {
      if (e.key === "Escape") onClose();
    };
    document.addEventListener("keydown", handleKey);

    const originalOverflow = document.body.style.overflow;
    document.body.style.overflow = "hidden";

    // focus close button for accessibility
    closeBtnRef.current?.focus();

    return () => {
      document.removeEventListener("keydown", handleKey);
      document.body.style.overflow = originalOverflow;
    };
  }, [isOpen, onClose]);

  // Memoize analysis so it doesn't recompute on every render
  const analysis = useMemo(
    () => (hive ? analyzeHive(hive, t) : null),
    [hive, t],
  );

  if (!isOpen || !hive || !analysis) return null;

  const healthColor = getHealthColor(analysis.healthScore);
  const healthLabel =
    analysis.healthScore >= 80
      ? t.excellent
      : analysis.healthScore >= 60
        ? t.good
        : t.poor;
  const healthDesc =
    analysis.healthScore >= 80
      ? t.healthExcellentDesc
      : analysis.healthScore >= 60
        ? t.healthGoodDesc
        : t.healthPoorDesc;

  const handleBackdropClick = (e) => {
    if (e.target === e.currentTarget) onClose();
  };

  return (
    <div
      className="fixed inset-0 bg-black/70 backdrop-blur-sm z-[100] flex items-center justify-center p-4 animate-fade-in"
      onClick={handleBackdropClick}
      role="dialog"
      aria-modal="true"
      aria-labelledby="ai-analysis-title"
    >
      <div className="bg-gray-900 border border-gray-700 rounded-lg max-w-3xl w-full max-h-[90vh] overflow-hidden shadow-2xl animate-scale-in">
        {/* Header */}
        <div className="flex items-center justify-between p-6 border-b border-gray-800 bg-gradient-to-r from-purple-500/10 to-pink-500/10">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 bg-purple-500/20 rounded-full flex items-center justify-center">
              <Sparkles className="w-6 h-6 text-purple-400" />
            </div>
            <div>
              <h3
                id="ai-analysis-title"
                className="text-lg font-semibold text-gray-100"
              >
                {t.title}
              </h3>
              <p className="text-sm text-gray-500">
                {t.hive} #{hive.id ?? "—"}
              </p>
            </div>
          </div>
          <button
            ref={closeBtnRef}
            onClick={onClose}
            aria-label={t.close}
            className="p-2 hover:bg-gray-800 rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-purple-500"
          >
            <X className="w-5 h-5 text-gray-400" />
          </button>
        </div>

        {/* Body */}
        <div className="p-6 overflow-y-auto max-h-[calc(90vh-80px)] custom-scroll">
          {/* Health Score */}
          <section className="mb-6">
            <h4 className="text-sm font-semibold text-gray-400 uppercase mb-3">
              {t.healthScore}
            </h4>
            <div className="flex items-center gap-4">
              <HealthRing
                score={analysis.healthScore}
                colorClass={healthColor.text}
              />
              <div>
                <p className="text-2xl font-bold text-gray-100 mb-1">
                  {healthLabel}
                </p>
                <p className="text-sm text-gray-500">{healthDesc}</p>
              </div>
            </div>
          </section>

          {/* Predictions */}
          <section className="mb-6">
            <h4 className="text-sm font-semibold text-gray-400 uppercase mb-3 flex items-center gap-2">
              <TrendingUp className="w-4 h-4" />
              {t.predictions}
            </h4>
            <div className="space-y-3">
              {analysis.predictions.map((pred, i) => (
                <PredictionCard key={`${pred.type}-${i}`} pred={pred} t={t} />
              ))}
            </div>
          </section>

          {/* Actions */}
          <section>
            <h4 className="text-sm font-semibold text-gray-400 uppercase mb-3 flex items-center gap-2">
              <CheckCircle className="w-4 h-4" />
              {t.actions}
            </h4>
            <div className="bg-gray-800 rounded-lg p-4">
              <ul className="space-y-2">
                {analysis.actions.map((action, i) => (
                  <li
                    key={i}
                    className="flex items-start gap-3 text-sm text-gray-300"
                  >
                    <span
                      className="text-amber-400 font-semibold"
                      aria-hidden="true"
                    >
                      {i + 1}.
                    </span>
                    <span>{action}</span>
                  </li>
                ))}
              </ul>
            </div>
          </section>

          {/* Disclaimer */}
          <div className="mt-6 p-4 bg-blue-500/10 border border-blue-500/30 rounded-lg">
            <p className="text-xs text-gray-400">
              💡 <strong>{t.note}:</strong> {t.disclaimer}
            </p>
          </div>
        </div>
      </div>
    </div>
  );
};

// ─── Subcomponents ───────────────────────────────────────────────────────
const HealthRing = ({ score, colorClass }) => {
  const RADIUS = 40;
  const CIRCUMFERENCE = 2 * Math.PI * RADIUS;
  const offset = CIRCUMFERENCE * (1 - score / 100);

  return (
    <div
      className="relative w-24 h-24"
      role="img"
      aria-label={`Health score ${score}`}
    >
      <svg className="transform -rotate-90 w-24 h-24">
        <circle
          cx="48"
          cy="48"
          r={RADIUS}
          stroke="currentColor"
          strokeWidth="8"
          fill="none"
          className="text-gray-800"
        />
        <circle
          cx="48"
          cy="48"
          r={RADIUS}
          stroke="currentColor"
          strokeWidth="8"
          fill="none"
          strokeDasharray={CIRCUMFERENCE}
          strokeDashoffset={offset}
          className={`${colorClass} transition-[stroke-dashoffset] duration-700`}
          strokeLinecap="round"
        />
      </svg>
      <div className="absolute inset-0 flex items-center justify-center">
        <span className="text-2xl font-bold text-gray-100">{score}</span>
      </div>
    </div>
  );
};

const PredictionCard = ({ pred, t }) => {
  const riskLabels = { high: t.riskHigh, medium: t.riskMedium, low: t.riskLow };
  const riskColors = {
    high: "text-red-400",
    medium: "text-amber-400",
    low: "text-emerald-400",
  };
  const typeLabels = {
    swarm: t.swarm,
    varroa: t.varroa,
    production: t.production,
    health: t.health,
  };

  return (
    <div className="bg-gray-800 rounded-lg p-4">
      <div className="flex items-start justify-between mb-2 gap-2">
        <h5 className="font-semibold text-gray-100">{typeLabels[pred.type]}</h5>
        {pred.risk && (
          <span
            className={`text-xs font-semibold uppercase whitespace-nowrap ${riskColors[pred.risk]}`}
          >
            {riskLabels[pred.risk]}
          </span>
        )}
      </div>

      {pred.probability && (
        <p className="text-sm text-gray-400 mb-1">
          {t.probability}:{" "}
          <span className="font-semibold text-gray-300">
            {pred.probability}
          </span>
          {pred.timeframe && <> ({pred.timeframe})</>}
        </p>
      )}

      {pred.value && (
        <p className="text-sm text-gray-400 mb-1">
          {t.estProduction}:{" "}
          <span className="font-semibold text-gray-300">{pred.value}</span>
        </p>
      )}

      {pred.issues?.length > 0 && (
        <div className="mt-2 space-y-1">
          {pred.issues.map((issue, j) => (
            <div
              key={j}
              className="flex items-center gap-2 text-sm text-red-400"
            >
              <AlertCircle className="w-3 h-3 flex-shrink-0" />
              <span>{issue}</span>
            </div>
          ))}
        </div>
      )}

      <p className="text-sm text-purple-400 mt-2 flex items-start gap-2">
        <Lightbulb className="w-4 h-4 flex-shrink-0 mt-0.5" />
        <span>{pred.recommendation}</span>
      </p>
    </div>
  );
};

// ─── Helpers ─────────────────────────────────────────────────────────────
const getHealthColor = (score) => {
  if (score >= 80) return { bg: "bg-emerald-500", text: "text-emerald-400" };
  if (score >= 60) return { bg: "bg-amber-500", text: "text-amber-400" };
  return { bg: "bg-red-500", text: "text-red-400" };
};

const clamp = (v, min, max) => Math.max(min, Math.min(max, v));
const sum = (arr) => arr.reduce((a, b) => a + b, 0);

export default AIAnalysisPanel;

