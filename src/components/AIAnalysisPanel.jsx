//  RULE-BASED AI ANALYSIS ENGINE
// ═══════════════════════════════════════════════════════════════════════════
function analyzeHive(hive, t) {
  const temp = Number(hive.temp) || 0;
  const humidity = Number(hive.humidity) || 0;
  const sound = Number(hive.sound) || 0;
  const vibration = Number(hive.vibration) || 0;
  const battery = hive.battery ?? 100;

  const issues = [];
  let healthScore = 100;

  // Temperature
  if (temp > 40) {
    healthScore -= 30;
    issues.push(t.issueTempCrit(temp));
  } else if (temp > 37) {
    healthScore -= 15;
    issues.push(t.issueTempHigh(temp));
  } else if (temp < 10) {
    healthScore -= 25;
    issues.push(t.issueTempVeryLow(temp));
  } else if (temp < 20) {
    healthScore -= 10;
    issues.push(t.issueTempLow(temp));
  }

  // Humidity
  if (humidity > 85) {
    healthScore -= 20;
    issues.push(t.issueHumExcess(humidity));
  } else if (humidity > 75) {
    healthScore -= 10;
    issues.push(t.issueHumHigh(humidity));
  } else if (humidity < 30) {
    healthScore -= 15;
    issues.push(t.issueHumVeryLow(humidity));
  }

  // Sound
  if (sound > 80) {
    healthScore -= 20;
    issues.push(t.issueSound(sound));
  } else if (sound > 60) {
    healthScore -= 8;
  }

  // Vibration
  if (vibration > 2000) {
    healthScore -= 20;
    issues.push(t.issueVib(vibration));
  } else if (vibration > 1000) {
    healthScore -= 10;
  }

  // Battery
  if (battery < 15) {
    healthScore -= 5;
    issues.push(t.issueBattery(battery));
  }

  healthScore = clamp(healthScore, 0, 100);

  // Swarm probability
  const swarmFactors = [];
  if (temp > 40) swarmFactors.push(0.5);
  else if (temp > 36) swarmFactors.push(0.3);
  if (humidity > 70) swarmFactors.push(0.2);
  if (sound > 65) swarmFactors.push(0.35);
  if (vibration > 1200) swarmFactors.push(0.25);
  const swarmProb = Math.min(0.95, sum(swarmFactors));
  const swarmRisk =
    swarmProb > 0.6 ? "high" : swarmProb > 0.3 ? "medium" : "low";

  // Varroa probability
  const varroaFactors = [];
  if (sound > 55 && sound < 75) varroaFactors.push(0.25);
  if (temp > 35 && humidity > 65) varroaFactors.push(0.2);
  if (vibration > 800 && vibration < 1500) varroaFactors.push(0.15);
  const varroaProb = Math.min(0.9, sum(varroaFactors));
  const varroaRisk =
    varroaProb > 0.5 ? "high" : varroaProb > 0.2 ? "medium" : "low";

  // Actions (deduplicated via Set)
  const actionSet = new Set();
  if (temp > 38) actionSet.add(t.actVentilate);
  if (temp < 15) actionSet.add(t.actInsulate);
  if (humidity > 80) actionSet.add(t.actDehumidify);
  if (humidity < 35) actionSet.add(t.actHumidify);
  if (swarmRisk === "high") actionSet.add(t.actSwarmCheck);
  if (varroaRisk !== "low") actionSet.add(t.actVarroaTest);
  if (battery < 20) actionSet.add(t.actBattery);
  if (actionSet.size === 0) actionSet.add(t.actRoutine);
  const actions = Array.from(actionSet);

  // Production estimate
  const prodFactor = healthScore > 80 ? 1.0 : healthScore > 60 ? 0.7 : 0.3;
  const weeklyProd = (3.0 * prodFactor).toFixed(1);
  const prodTrend =
    healthScore > 80 ? "up" : healthScore > 60 ? "stable" : "down";

  return {
    healthScore,
    predictions: [
      {
        type: "swarm",
        risk: swarmRisk,
        probability: `${Math.round(swarmProb * 100)}%`,
        timeframe:
          swarmRisk === "high"
            ? t.swarmHighTf
            : swarmRisk === "medium"
              ? t.swarmMedTf
              : t.swarmLowTf,
        recommendation:
          swarmRisk === "high"
            ? t.swarmHighRec
            : swarmRisk === "medium"
              ? t.swarmMedRec
              : t.swarmLowRec,
      },
      {
        type: "varroa",
        risk: varroaRisk,
        probability: `${Math.round(varroaProb * 100)}%`,
        timeframe: t.varroaMethod,
        recommendation:
          varroaRisk === "high"
            ? t.varroaHighRec
            : varroaRisk === "medium"
              ? t.varroaMedRec
              : t.varroaLowRec,
      },
      {
        type: "production",
        value: `${weeklyProd} kg/${t.week}`,
        trend: prodTrend,
        recommendation:
          prodTrend === "up"
            ? t.prodUpRec
            : prodTrend === "stable"
              ? t.prodStableRec
              : t.prodDownRec,
      },
      {
        type: "health",
        status:
          healthScore > 80 ? "good" : healthScore > 50 ? "moderate" : "poor",
        issues,
        recommendation:
          healthScore > 80
            ? t.healthGoodRec
            : healthScore > 50
              ? t.healthModRec
              : t.healthPoorRec,
      },
    ],
    actions,
  };
}
